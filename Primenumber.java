public class Primenumber{
	public static void main(String[] args) {
		int n=3;
		if(n%2==0)
		{
			System.out.println(n+"is not a prime number");
		}if(n==1||n==0)
		{
			System.out.println(n+"is not a prime number");
		}else
		{
			System.out.println(n+"is a prime number");
		}
	}
	}
